    public static void switchErrorCode(Context context, String code) {
        mContext = context;
        for (int i = 0; i < codes.length; i++) {
            if (codes[i].equals(code)) {
                showToast(meanings[i]);
//                Toast.makeText(context, codes.length + "-->" + meanings.length, Toast.LENGTH_SHORT).show();
            }
        }
    }

    private static void showToast(String s) {
        Toast.makeText(mContext, s, Toast.LENGTH_SHORT).show();
    }
